    private void btnTimKiemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnTimKiemActionPerformed
        String nameSearch = (String) conboNameCar.getSelectedItem();
        String date = null;
        if (!"".equals(txtDate.getText())) {
            Pattern pattern = Pattern.compile("[0-9]{4}\\-[0-9]{1,2}\\-[0-9]{1,2}");
            Matcher matcher = pattern.matcher(txtDate.getText());
            if(pattern.matcher(txtDate.getText()).matches()){
                date = txtDate.getText();
            }else{
                JOptionPane.showMessageDialog(this, "Nhập ngày tháng theo đúng định dạng yyyy-MM-dd");
            }
        } else {
            JOptionPane.showMessageDialog(this, "Ngày tháng không được để trống");
        }

        items = new CarsDAO().getListItemSearch(nameSearch, date);
        table = (DefaultTableModel) tblKQTK.getModel();
        table.setColumnIdentifiers(header);
        ShowTable();
        
    }//GEN-LAST:event_btnTimKiemActionPerformed

