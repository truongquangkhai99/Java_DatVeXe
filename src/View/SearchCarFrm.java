    private void btnDatGheActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDatGheActionPerformed
        selectedIndex = tblKQTK.getSelectedRow();
        if (selectedIndex == -1) {
            JOptionPane.showMessageDialog(this, "vui lòng chọn xe");

        } else {
            DatGhe form = new DatGhe();
            form.showDiemDon(items.get(selectedIndex), maKhachHang);
            form.showGhe((items.get(selectedIndex)));
            form.setVisible(true);
        }
    }//GEN-LAST:event_btnDatGheActionPerformed

