    public void showComboData() {
        ArrayList<Cars> itemsCars = new ArrayList<>();
        itemsCars = new CarsDAO().getListItemName();
        for (Cars i : itemsCars) {
            conboNameCar.addItem(i.getName());
        }
    }

    public void ShowTable() {
        table.setRowCount(0);
        for (Cars i : items) {
            table.addRow(new Object[]{
                table.getRowCount() + 1, i.getNumber_car(), i.getName(), new SimpleDateFormat("yyyy-MM-dd").format(i.getDate()), i.getTime(), i.getPrice() + " VNĐ"
            });
        }
    }

