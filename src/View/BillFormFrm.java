    private void btnUpdateActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnUpdateActionPerformed
        int indexGhe = cmbGhe.getSelectedIndex();
        int indexDiemDon = cmbDiemDon.getSelectedIndex();
        int place_id = dsDiemDon.get(indexDiemDon).getPlace_id();
        int position_id = 0;
        boolean isOk = true;
        
        if (indexGhe == -1) {
            JOptionPane.showMessageDialog(this, "Ghế hiện tại không có sẵn vui lòng chọn xe khác");
            isOk = false;
        } else {
            position_id = dsGhe.get(indexGhe).getPosition_id();
        }
        if (isOk) {
            
            if (new BillDAO().UpdateItem(position_id, place_id, BillFrm.item.getBill_id()) && new PositionDAO().updateStatus(position_id) && new PositionDAO().updateStatus0(BillFrm.itemBill.getPosition_id())) {
                JOptionPane.showMessageDialog(this, "Cập nhật thành công");
                this.setVisible(false);
                home.setVisible(false);
                new BillFrm().setVisible(true);
                
            } else {
                JOptionPane.showMessageDialog(this, "có lỗi khi cập nhật");
            }
        }
    }//GEN-LAST:event_btnUpdateActionPerformed

