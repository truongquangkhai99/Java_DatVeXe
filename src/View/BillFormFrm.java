    public void showDiemDon() {
        dsDiemDon = new PlaceDAO().getListItemCars(BillFrm.item.getNumber_car());
        for (Place i : dsDiemDon) {
            cmbDiemDon.addItem(i.getName());
        }
        cmbDiemDon.setSelectedItem(BillFrm.item.getName_place());
    }

    public void showGhe() {
        dsGhe = new PositionDAO().getListItemByBill(BillFrm.item.getNumber_car(), BillFrm.itemBill.getPosition_id());
        for (Position i : dsGhe) {
            cmbGhe.addItem(i.getName());
        }
        cmbGhe.setSelectedItem(BillFrm.item.getName_posi());
    }

