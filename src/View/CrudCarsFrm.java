    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        selectedIndex = tblCars.getSelectedRow();
        if (selectedIndex == -1) {
            JOptionPane.showMessageDialog(this, "vui lòng chọn xe");
        } else {
            int isDelete = JOptionPane.showConfirmDialog(this, "Bạn có chắc chắn muốn xóa không");
            if (isDelete == 0) {
                if (new CarsDAO().deleteItem(items.get(selectedIndex))) {
                    items.remove(selectedIndex);
                    JOptionPane.showMessageDialog(this, "Xóa thành công");
                    ShowTable();
                } else {
                    JOptionPane.showMessageDialog(this, "Xe đã được đặt, không thể xóa");
                }
            }
        }

    }//GEN-LAST:event_btnDeleteActionPerformed

