    public void ShowTable() {
        model.setRowCount(0);
        for (Cars i : items) {
            model.addRow(new Object[]{
                model.getRowCount() + 1, i.getNumber_car(), i.getName(), new SimpleDateFormat("yyyy-MM-dd").format(i.getDate()), i.getTime(), i.getPrice() + " VNĐ"
            });
        }
    }

    public void addItem(Cars item) {
        items.add(item);
        ShowTable();
    }

    public void updateItem(Cars item) {
        items.remove(selectedIndex);
        items.add(item);
        ShowTable();

    }

